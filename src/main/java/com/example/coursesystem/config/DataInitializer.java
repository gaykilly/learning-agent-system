    private KnowledgeBase createKnowledge(String courseName, Integer chapterNo, String chapterName,
                                          String knowledgeNo, String knowledgeName, String content,
                                          String type, Integer difficulty, String keywords) {
        KnowledgeBase k = new KnowledgeBase();
        k.setCourseName(courseName);
        k.setChapterNo(chapterNo);
        k.setChapterName(chapterName);
        k.setKnowledgeNo(knowledgeNo);
        k.setKnowledgeName(knowledgeName);
        k.setContent(content);
        k.setType(type);
        k.setDifficulty(difficulty);
        k.setKeywords(keywords);
        return k;
    }
